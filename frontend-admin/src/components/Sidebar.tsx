import { NavLink, useNavigate } from 'react-router-dom'
import { useAuth } from '@/context/AuthContext'

const navItems = [
  { to: '/', label: 'Dashboard', icon: DashboardIcon },
  { to: '/catalog', label: 'Catalog', icon: CatalogIcon },
  { to: '/channels', label: 'Channels', icon: ChannelsIcon },
  { to: '/schedule', label: 'Schedule', icon: ScheduleIcon },
  { to: '/users', label: 'Users', icon: UsersIcon },
  { to: '/packages', label: 'Packages & Offers', icon: PackagesIcon },
  { to: '/streaming', label: 'Streaming', icon: StreamingIcon },
  { to: '/analytics', label: 'Analytics', icon: AnalyticsIcon },
]

export default function Sidebar() {
  const { logout, user } = useAuth()
  const navigate = useNavigate()

  function handleLogout() {
    logout()
    navigate('/login')
  }

  return (
    <aside className="fixed inset-y-0 left-0 z-30 flex w-64 flex-col border-r border-gray-200 bg-white">
      {/* Brand */}
      <div className="flex h-16 items-center gap-2 border-b border-gray-200 px-6">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-indigo-600 text-sm font-bold text-white">
          O
        </div>
        <span className="text-lg font-semibold text-gray-900">OTT Admin</span>
      </div>

      {/* Navigation */}
      <nav className="flex-1 space-y-1 overflow-y-auto px-3 py-4">
        {navItems.map((item) => {
          const Icon = item.icon
          return (
            <NavLink
              key={item.to}
              to={item.to}
              end={item.to === '/'}
              className={({ isActive }) =>
                `flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-indigo-50 text-indigo-700'
                    : 'text-gray-700 hover:bg-gray-50 hover:text-gray-900'
                }`
              }
            >
              <Icon />
              {item.label}
            </NavLink>
          )
        })}
      </nav>

      {/* User / Logout */}
      <div className="border-t border-gray-200 p-4">
        <div className="mb-2 truncate text-sm text-gray-500">
          {user?.email}
        </div>
        <button
          onClick={handleLogout}
          className="flex w-full items-center gap-2 rounded-lg px-3 py-2 text-sm font-medium text-gray-700 transition-colors hover:bg-gray-50 hover:text-gray-900"
        >
          <LogoutIcon />
          Sign out
        </button>
      </div>
    </aside>
  )
}

